#include <iostream>

using namespace std;

int main()
{
    // output | printing to terminal
    // syntax
    // cout << variable | value ;

    cout << "Krish";         // output | print without next line
    cout << "Krish" << endl; // output | print with next line
    cout << "Krish" << "\n"; // output | print with next line (preferred)

    // input from terminal
    // syntax
    // cin >> variable;
    int a;
    cin >> a;          // taking input
    cout << a << endl; // output | print with next line

    return 0;
}
